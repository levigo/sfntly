package org.jadice.font.tools.conversion.eot;

import org.jadice.font.sfntly.Font;
import org.jadice.font.sfntly.data.WritableFontData;
import org.jadice.font.sfntly.testutils.TestFont;
import org.jadice.font.sfntly.testutils.TestFontUtils;
import org.jadice.font.sfntly.testutils.TestUtils;
import java.io.File;
import java.io.IOException;
import junit.framework.TestCase;

/** @author Raph Levien */
public class EOTWriterTest extends TestCase {
  private static final File fontFile = TestFont.TestFontNames.OPENSANS.getFile();
  private static final long EOT_VERSION = 0x00020002;

  public void testBasicEot() throws IOException {
    Font srcFont = TestFontUtils.loadFont(fontFile)[0];

    EOTWriter eotWriter = new EOTWriter();
    WritableFontData eotData = eotWriter.convert(srcFont);

    // This hash is generated by commit 8c0954e.
    TestUtils.assertSha256(
        "82b9c32f9e2b4c133b28883ddcaff84fd3d4247adb2ea87a5d08c7d034ffaa6f", eotData);

    assertEquals(eotData.length(), eotData.readULongLE(0));
    assertEquals(EOT_VERSION, eotData.readULongLE(8));
    // TODO: more sanity-checking and validation
  }
}
